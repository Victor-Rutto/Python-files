#Bitwise_operators.
# Work on Bit numbers
# & and
# / or
# ~ Not (complement)
# >> Right shif
# << Left shift
# ^ XOR 


# Working with 4 bit representation
a, b = 5, 4
# & and
print( a & b)

# / or
print(a|b)

# ~ Not (complement)
print(~a)

# >> Right shif
print(a>>2)

# << Left shift
print(a<<2)

# ^ XOR 
print(a^b)

c = 26
d = 23
print(c&d)
print(68<<2)
print(~45)
