#ROUNDING OFF FUNCTION
# We use the feature in python instead of using int
# round(number, number of digits)

print(round(21.3333, 2))
print(round(7))
print(type(round(7, 2)))

print(round(7.888))

#NOTE: ROUNDING OFF A VALUE LIKE 6.5 WILL ROUND IT TO THE NEAREST EVEN NUMBER 
print(round(7.5))
print(round(6.5))

#ROUNDING OFF WITH NEGATIVE NUMBER OF DIGITS
print(round(674.555, -2))

print(round(-8/3, 2))
print(round(-880/3, -1))




