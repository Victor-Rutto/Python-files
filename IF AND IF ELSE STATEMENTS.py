#IF AND IF ELSE STATEMENTS
height = int(input("enter height in feet "))

#NOTE: when using if and else, the whole code between should be indented
if(height>3):
    print("Buy token")
    print("Now you can board the metro")
else:
    print("No token required")
    print("Welcome to the metro, enjoy your ride")

#When we only have if statement
if height>3: #You can use the brackets or not if(height>3):
    print("Buy token")
print("out of if statement")







